# statistics
from statistics import *

s = mean([43, 67, 89, 21, 47])
print(s)
x = median([56, 78, 90, 69, 82, 71])   # 56, 69, 71, 78, 82, 90
print(x)
r = mode([78, 91, 59, 82, 91, 64, 90, 78, 91, 78, 91])   # 59, 64, 78, 78, 78, 82, 90, 91, 91, 91, 91
print(r)
w = variance([78, 91, 59, 82, 91, 64, 90, 78, 91])
print(w)
z = stdev([78, 91, 59, 82, 91, 64, 90, 78, 91])
print(z)
